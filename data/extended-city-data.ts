/**
 * Get extended city data by state and city slugs
 */
export function getExtendedCityData(
  stateSlug: string,
  citySlug: string
): ExtendedCityData | undefined {
  return extendedCities.find(
    (city) => city.stateSlug === stateSlug && city.slug === citySlug
  );
}

/**
 * Get all extended cities in a state
 */
export function getExtendedCitiesByState(stateSlug: string): ExtendedCityData[] {
  return extendedCities.filter((city) => city.stateSlug === stateSlug);
}

/**
 * Get extended cities by climate zone
 */
export function getExtendedCitiesByClimate(
  climate: ExtendedCityData["climate"]
): ExtendedCityData[] {
  return extendedCities.filter((city) => city.climate === climate);
}

/**
 * Get all unique states from extended cities
 */
export function getExtendedUniqueStates(): Array<{
  state: string;
  stateSlug: string;
  stateAbbr: string;
}> {
  const stateMap = new Map<string, { state: string; stateAbbr: string }>();
  extendedCities.forEach((city) => {
    if (!stateMap.has(city.stateSlug)) {
      stateMap.set(city.stateSlug, { state: city.state, stateAbbr: city.stateAbbr });
    }
  });
  return Array.from(stateMap.entries()).map(([stateSlug, data]) => ({
    state: data.state,
    stateSlug,
    stateAbbr: data.stateAbbr,
  }));
}

/**
 * Calculate material cost for a specific city and base material
 */
export function getCityMaterialCost(
  stateSlug: string,
  citySlug: string,
  materialType: keyof ExtendedCityData["materialCosts"],
  baseCost: number
): number {
  const city = getExtendedCityData(stateSlug, citySlug);
  if (!city) return baseCost;

  return baseCost * city.materialCosts[materialType];
}

/**
 * Calculate labor cost for a specific city
 */
export function getCityLaborCost(
  stateSlug: string,
  citySlug: string,
  baseLaborCost: number
): number {
  const city = getExtendedCityData(stateSlug, citySlug);
  if (!city) return baseLaborCost;

  return baseLaborCost * city.laborRate;
}
